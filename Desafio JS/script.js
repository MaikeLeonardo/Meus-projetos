
let numberOne = prompt("Digite o 1° numero")
let numberTwo = prompt("Digite o 2° numero")
let sun = Number(numberOne) + Number(numberTwo)
let sub = Number(numberOne) - Number(numberTwo)
let mult = Number(numberOne) * Number(numberTwo)
let divi = Number(numberOne) / Number(numberTwo)
let res = Number(numberOne) % Number(numberTwo)


alert ("A soma dos dois é igual á : " + sun)
alert ("A subtração dos dois é igual á : " + sub)
alert ("A multiplicação dos dois é igual á : " + mult)
alert ("A divisão dos dois é igual á : " + divi)
alert ("O resto dos dois é igual á : " + res)


